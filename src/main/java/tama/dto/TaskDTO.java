package tama.dto;

public class TaskDTO {

    private int id;
    private String name;

    public TaskDTO() {
    }

    public TaskDTO(int id, String name) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
